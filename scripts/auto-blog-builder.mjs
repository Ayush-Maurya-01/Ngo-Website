import { GoogleGenerativeAI } from '@google/generative-ai';
import { createClient } from '@supabase/supabase-js';
import crypto from 'crypto';

// Initialize APIs
const GEMINI_API_KEY = process.env.GEMINI_API_KEY;
const SUPABASE_URL = process.env.NEXT_PUBLIC_SUPABASE_URL;
const SUPABASE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!GEMINI_API_KEY || !SUPABASE_URL || !SUPABASE_KEY) {
  console.error("❌ Missing required environment variables.");
  process.exit(1);
}

const genAI = new GoogleGenerativeAI(GEMINI_API_KEY);
const supabase = createClient(SUPABASE_URL, SUPABASE_KEY);

const TOPICS = [
  "Women Empowerment in Rural India",
  "Child Education Rights and Awareness",
  "Free Legal Aid Services for the Underprivileged",
  "Environmental Protection & Climate Change Impacts",
  "Understanding Fundamental Human Rights",
  "The Importance of Community Health Camps",
  "Tribal Rights and Protection in India",
  "Right to Information Act: Empowering Citizens",
  "Eradicating Child Labour through Education",
  "Mental Health Awareness in Marginalized Communities"
];

async function generateBlog() {
  console.log("🤖 Starting Autonomous AI Blog Generation...");
  
  // 1. Pick a random topic to base the article on
  const randomTopic = TOPICS[Math.floor(Math.random() * TOPICS.length)];
  console.log(`📌 Selected Theme: ${randomTopic}`);

  // 2. Generate Content using Gemini
  const model = genAI.getGenerativeModel({ model: "gemini-2.5-flash" });
  const prompt = `
    You are an expert human rights activist, policy analyst, and SEO blog writer for PCHR&R (Possible Centre for Human Rights & Responsibilities), a top NGO in India.
    
    Write an extremely detailed, deeply researched, and highly educational article about: "${randomTopic}". 
    The tone should be authoritative, informative, and perfectly suited for an NGO's audience. 
    Before writing, perform a deep analysis of the topic. Ensure every piece of information, statistic, or claim is factually correct.
    
    CRITICAL INSTRUCTIONS FOR FORMATTING & LENGTH:
    - The article MUST be VERY LONG (at least 1000 - 1500 words).
    - Provide a comprehensive step-by-step or detailed breakdown of the topic.
    - Use EXACTLY this structure: a strong introductory section, followed by 4-6 detailed sections with clear <h2> headings. Use <h3> subheadings where necessary.
    - Include at least two <blockquote> tags for powerful statements, expert quotes, or legal provisions.
    - Include multiple <ul> or <ol> (bulleted/numbered lists) summarizing key points, actionable steps, or historical facts.
    - Use <strong> tags naturally to emphasize important keywords and concepts.
    - Do not mention that this was generated by AI. Write as a human expert.
    
    Format the output EXACTLY as a raw JSON object string with these exact keys. DO NOT use markdown code blocks like \`\`\`json, just output the raw JSON directly:
    {
      "title": "A highly attractive, deeply informative, click-worthy SEO title under 65 characters",
      "category": "One of: Human Rights, Education, Health, Social Awareness",
      "read_time": "Estimated read time (e.g., '10 min read')",
      "content": "The full, extremely long article content formatted in valid HTML with <h2>, <h3>, <p>, <blockquote>, <ul>, <ol>, <li>, and <strong> tags."
    }
  `;

  console.log("✍️ AI is writing the article...");
  let result;
  try {
    result = await model.generateContent(prompt);
  } catch(err) {
    console.error("❌ Failed to contact Gemini API", err);
    process.exit(1);
  }

  let responseText = result.response.text();
  responseText = responseText.replace(/```json/g, '').replace(/```/g, '').trim();
  
  let blogData;
  try {
    blogData = JSON.parse(responseText);
  } catch (e) {
    console.error("❌ Failed to parse AI response as JSON:", responseText);
    process.exit(1);
  }

  // 3. Select a Guaranteed High-Quality Cover Image
  console.log("🎨 Selecting dynamic cover image...");
  
  // A curated list of high-quality Unsplash images representing NGO work in India (Education, Health, Women, Rights)
  const NGO_IMAGES = [
    "https://images.unsplash.com/photo-1488521787991-ed7bbaae773c?q=80&w=2070&auto=format&fit=crop", // Kids education
    "https://images.unsplash.com/photo-1532629345422-7515f3d16bb6?q=80&w=2070&auto=format&fit=crop", // Charity hands
    "https://images.unsplash.com/photo-1509062522246-3755977927d7?q=80&w=2070&auto=format&fit=crop", // Classroom/Education
    "https://images.unsplash.com/photo-1542810634-71277d95dcbb?q=80&w=2070&auto=format&fit=crop", // Smiley kids
    "https://images.unsplash.com/photo-1593113646773-028c64a8f1b8?q=80&w=2070&auto=format&fit=crop", // Rural health/people
    "https://images.unsplash.com/photo-1518398046578-8cca57782e17?q=80&w=2070&auto=format&fit=crop", // Volunteering
    "https://images.unsplash.com/photo-1594708767771-a7502209ff51?q=80&w=2070&auto=format&fit=crop", // Women/Empowerment
    "https://images.unsplash.com/photo-1528605248644-14dd04022da1?q=80&w=2070&auto=format&fit=crop"  // Community/Village
  ];
  
  // Pick a random image from the curated list to ensure it ALWAYS loads beautifully
  const imageUrl = NGO_IMAGES[Math.floor(Math.random() * NGO_IMAGES.length)];

  // 4. Insert into Supabase
  console.log("💾 Saving article to the database...");
  const newPost = {
    id: crypto.randomUUID(),
    title: blogData.title,
    content: blogData.content,
    category: blogData.category,
    read_time: blogData.read_time,
    image_url: imageUrl,
    author: "PCHR&R AI Editorial",
    created_at: new Date().toISOString()
  };

  const { error } = await supabase.from('blog_posts').insert([newPost]);
  
  if (error) {
    console.error("❌ Supabase Database Error:", error.message);
    process.exit(1);
  }

  console.log("✅ Success! Blog post published live.");
  console.log(`📑 Title: ${blogData.title}`);
  console.log(`📁 Category: ${blogData.category}`);
  console.log(`🖼️ Image: ${imageUrl}`);
}

generateBlog().catch(err => {
  console.error("❌ Fatal Error:", err);
  process.exit(1);
});
